    private void initView(){
        btn_con = (Button) getActivity().findViewById(R.id.btn_con);
        btn_con.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                Intent enableIntent = new Intent(BluetoothAdapter.ACTION_REQUEST_ENABLE);
                startActivityForResult(enableIntent, REQUEST_ENABLE_BT);
            }
        });
    }

    @Override
    public void onActivityResult(int requestCode, int resultCode, Intent data) {
        super.onActivityResult(requestCode, resultCode, data);
        if (requestCode == REQUEST_ENABLE_BT){
            if (resultCode == Activity.RESULT_OK){
                if (!getActivity().getPackageManager().hasSystemFeature(PackageManager.FEATURE_BLUETOOTH_LE)){
                    Toast.makeText(getActivity(),"您的设备不支持低功耗蓝牙技术",Toast.LENGTH_SHORT).show();
                }else{
                    Intent intent = new Intent(getActivity(), BluetoothListActivity.class);
                    startActivity(intent);
                }
            }else{
                Toast.makeText(getActivity(),"无法开启蓝牙，无法连接",Toast.LENGTH_SHORT).show();
            }
        }
    }

